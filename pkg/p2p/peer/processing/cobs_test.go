func TestDecodeInvalid(t *testing.T) {
	testCases := []struct {
		title string
		data  []byte
	}{
		{
			title: "empty",
		},
		{
			title: "zero length",
			data:  []byte{0},
		},
		{
			title: "one off",
			data:  []byte{0x03, 'x'},
		},
		{
			title: "oversized block",
			data:  []byte{255, 'x'},
		},
		{
			title: "second oversized block",
			data:  []byte{1, 255, 'x'},
		},
	}
	for _, tc := range testCases {
		t.Run(tc.title, func(t *testing.T) {
			if got := Decode(tc.data); got != nil {
				if got.Bytes() != nil {
					t.Errorf("got %v, want nil", got.Bytes())
				}
			}
		})
	}
}

func TestRandom(t *testing.T) {
	f := func(p []byte) bool {
		// Make sure the bytes will be the same, encoded and decoded.
		// This needs to happen because the COBS decoder will add the last 0
		// to the decoded bytes, as the encoder adds it before starting.
		if len(p) > 0 && p[len(p)-1] != 0 {
			p = append(p, 0)
		}
		buf := bytes.NewBuffer(p)
		enc := Encode(buf).Bytes()
		dec := Decode(enc).Bytes()
		return bytes.Equal(dec, p)
	}
	if err := quick.Check(f, nil); err != nil {
		t.Error(err)
	}
}
